package zLuck.zAPIs;

public class Status {
	
	// 
	
	private String uuid;
	private int kills;
	private int deaths;
	private int streak;
	private int cash;
	private int coins;
	private int xp;
	private int vencedor;
	private int networkatual;
	
	public String getUuid() {
		return uuid;
	}
	public void setUuid(String uuid) {
		this.uuid = uuid;
	}
	public int getKills() {
		return kills;
	}
	public void setKills(int kills) {
		this.kills = kills;
	}
	public int getDeaths() {
		return deaths;
	}
	public void setDeaths(int deaths) {
		this.deaths = deaths;
	}
	public int getStreak() {
		return streak;
	}
	public void setStreak(int streak) {
		this.streak = streak;
	}
	public int getCash() {
		return cash;
	}
	public void setCash(int cash) {
		this.cash = cash;
	}
	public int getCoins() {
		return coins;
	}
	public void setCoins(int coins) {
		this.coins = coins;
	}
	public int getXp() {
		return xp;
	}
	public void setXp(int xp) {
		this.xp = xp;
	}
	public int getVencedor() {
		return vencedor;
	}
	public void setVencedor(int vencedor) {
		this.vencedor = vencedor;
	}
	public int getNetworkAtual() {
		return networkatual;
	}
	public void setNetworkAtual(int networkatual) {
		this.networkatual = networkatual;
	}

}
