package br.com.piracraft.lobby2.extencoes;

public class LojaMinecraft {

	private int id;
	private int item;
	private int variacao;
	private String nomeBukkit;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public int getItem() {
		return item;
	}

	public void setItem(int item) {
		this.item = item;
	}

	public int getVariacao() {
		return variacao;
	}

	public void setVariacao(int variacao) {
		this.variacao = variacao;
	}

	public String getNomeBukkit() {
		return nomeBukkit;
	}

	public void setNomeBukkit(String nomeBukkit) {
		this.nomeBukkit = nomeBukkit;
	}

}
