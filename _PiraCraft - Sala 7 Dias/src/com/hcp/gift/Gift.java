package com.hcp.gift;

import org.bukkit.Location;
import org.bukkit.block.Block;
import org.bukkit.inventory.ItemStack;

public class Gift {
	
	private Location localizacao;
	private ItemStack[] itens;
	private Block bloco;
	
	public Location getLocalizacao() {
		return localizacao;
	}
	
	public void setLocalizacao(Location localizacao) {
		this.localizacao = localizacao;
	}
	
	public ItemStack[] getItens() {
		return itens;
	}
	
	public void setItens(ItemStack[] itens) {
		this.itens = itens;
	}

	public Block getBloco() {
		return bloco;
	}

	public void setBloco(Block fb) {
		this.bloco = fb;
	}

}
