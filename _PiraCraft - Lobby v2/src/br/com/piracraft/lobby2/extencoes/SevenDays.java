package br.com.piracraft.lobby2.extencoes;

import java.util.ArrayList;
import java.util.List;

public class SevenDays {

	public static List<SevenDays> disponiveis = new ArrayList<SevenDays>();
	public static List<SevenDays> reviver = new ArrayList<SevenDays>();

	private String servidor;
	private int disponivel;
	private int comprasDisponiveis;
	private int custoIngresso;
	private int reviverDisponivel;
	private int custoReviver;
	private String premio;
	private String dataFinal;

	public int getDisponivel() {
		return disponivel;
	}

	public void setDisponivel(int disponivel) {
		this.disponivel = disponivel;
	}

	public String getServidor() {
		return servidor;
	}

	public void setServidor(String servidor) {
		this.servidor = servidor;
	}

	public int getComprasDisponiveis() {
		return comprasDisponiveis;
	}

	public void setComprasDisponiveis(int comprasDisponiveis) {
		this.comprasDisponiveis = comprasDisponiveis;
	}

	public int getCustoIngresso() {
		return custoIngresso;
	}

	public void setCustoIngresso(int custoIngresso) {
		this.custoIngresso = custoIngresso;
	}

	public int getReviverDisponivel() {
		return reviverDisponivel;
	}

	public void setReviverDisponivel(int reviverDisponivel) {
		this.reviverDisponivel = reviverDisponivel;
	}

	public int getCustoReviver() {
		return custoReviver;
	}

	public void setCustoReviver(int custoReviver) {
		this.custoReviver = custoReviver;
	}

	public String getPremio() {
		return premio;
	}

	public void setPremio(String premio) {
		this.premio = premio;
	}

	public String getDataFinal() {
		return dataFinal;
	}

	public void setDataFinal(String dataFinal) {
		this.dataFinal = dataFinal;
	}

}
